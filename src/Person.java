
public class Person {
	String name;
	String superpower;

	public Person(String name, String superpower) {
		this.name = name;
		this.superpower = superpower;

	}

	public void setName(String name) {
		this.name = name;
	}

	public void setSuperpower(String superpower) {
		this.superpower = superpower;
	}

}
